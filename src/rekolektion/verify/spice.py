# Write margin testbench
# ---------------------------------------------------------------------------

WRITE_MARGIN_TEMPLATE = Template("""\
* Write Margin Testbench
* Generated by rekolektion
*
* Determines minimum BL voltage needed to flip the cell state.
* Initial state: Q=VDD, QB=0. BL pulled low, BLB=VDD, WL activated.
* Sweep BL downward and find the trip point.
*

.param vdd_val = ${vdd}
.param temp_val = ${temp}

.lib "${pdk_root}/sky130A/${model_path}" ${corner}
.include "${bitcell_spice}"

Vvdd VDD 0 DC vdd_val
Vvss VSS 0 DC 0

* Word line ON
Vwl WL 0 DC vdd_val

* BLB held at VDD, BL swept down
Vblb BLB 0 DC vdd_val
Vbl  BL  0 DC vdd_val

* Instantiate and initialize cell (Q=VDD, QB=0)
Xcell BL BLB WL VDD VSS ${subckt_name}
.ic V(Xcell.Q) = vdd_val
.ic V(Xcell.QB) = 0

* Sweep BL from VDD down to 0
.dc Vbl vdd_val 0 -0.01

.meas DC write_trip FIND V(BL) WHEN V(Xcell.Q)=V(Xcell.QB)
.meas DC write_margin PARAM='vdd_val - write_trip'

.control
run
wrdata ${output_prefix}_write_margin.csv V(Xcell.Q) V(Xcell.QB)
.endc

.end
""")


