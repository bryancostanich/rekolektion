def generate_testbenches(
    bitcell_spice: str | Path,
    output_dir: str | Path = ".",
    corners: list[str] | None = None,
    voltages: list[float] | None = None,
    temperatures: list[float] | None = None,
) -> list[Path]:
    """Generate all characterization testbenches for the 6T bitcell.

    Args:
        bitcell_spice: Path to the bitcell SPICE netlist.
        output_dir: Directory for generated testbench files.
        corners: Process corners to sweep. Default: ["tt", "ss", "ff"].
        voltages: Supply voltages. Default: [1.62, 1.8, 1.98].
        temperatures: Temperatures in C. Default: [-40, 27, 125].

    Returns:
        List of paths to generated testbench files.
    """
    if corners is None:
        corners = ["tt", "ss", "ff"]
    if voltages is None:
        voltages = [RULES.VDD_MIN, RULES.VDD_NOMINAL, RULES.VDD_MAX]
    if temperatures is None:
        temperatures = [-40.0, 27.0, 125.0]

    output_dir = Path(output_dir)
    output_dir.mkdir(parents=True, exist_ok=True)
    bitcell_spice = Path(bitcell_spice).resolve()

    # Extract subcircuit name from the netlist file
    subckt_name = "sky130_sram_6t_bitcell"  # default
    for line in bitcell_spice.read_text().splitlines():
        if line.strip().lower().startswith(".subckt"):
            subckt_name = line.split()[1]
            break

    generated: list[Path] = []

    templates = {
        "read_snm": READ_SNM_TEMPLATE,
        "write_margin": WRITE_MARGIN_TEMPLATE,
        "hold_snm": HOLD_MARGIN_TEMPLATE,
        "transient": TRANSIENT_TEMPLATE,
    }

    import os
    pdk_root = os.environ.get("PDK_ROOT", os.path.expanduser("~/.volare"))

    for corner in corners:
        model_lib = SPICE_MODELS.get(corner, SPICE_MODELS["tt"])
        model_path, corner_name = model_lib.rsplit(" ", 1)

        for vdd in voltages:
            for temp in temperatures:
                params = {
                    "vdd": f"{vdd:.2f}",
                    "temp": f"{temp:.1f}",
                    "corner": corner_name,
                    "model_path": model_path,
                    "bitcell_spice": str(bitcell_spice),
                    "output_prefix": f"{corner}_{vdd:.2f}V_{temp:.0f}C",
                    "subckt_name": subckt_name,
                    "pdk_root": pdk_root,
                }

                for name, template in templates.items():
                    filename = f"tb_{name}_{corner}_{vdd:.2f}V_{temp:.0f}C.spice"
                    path = output_dir / filename
                    path.write_text(template.substitute(params))
                    generated.append(path)

    return generated


def run_ngspice(testbench_path: str | Path, timeout: int = 120) -> Path:
    """Run a single ngspice testbench.

    Returns path to the output data file.
    """
    import subprocess

    tb = Path(testbench_path)
    log = tb.with_suffix(".log")

    try:
        result = subprocess.run(
            ["ngspice", "-b", str(tb)],
            capture_output=True,
            text=True,
            timeout=timeout,
            cwd=str(tb.parent),
        )
    except FileNotFoundError:
        raise RuntimeError("ngspice not found on PATH. Install ngspice.")

    log.write_text(result.stdout + "\n" + result.stderr)

    if result.returncode != 0:
        raise RuntimeError(f"ngspice failed on {tb.name}. See {log}")

    return log
