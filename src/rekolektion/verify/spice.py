"""SPICE characterization testbenches for 6T SRAM bitcell.

Generates ngspice testbenches for:
1. Static Noise Margin (SNM) — butterfly curve / read margin
2. Write margin — minimum voltage to flip the cell
3. Hold margin — stability with word line off
4. Read/write access time
5. PVT corner sweeps (TT, SS, FF, SF, FS at VDD ± 10%)
"""

from pathlib import Path
from string import Template

from rekolektion.tech.sky130 import (
    NMOS_MODEL, PMOS_MODEL, RULES, SPICE_MODELS,
)


def _pdk_model_path(corner: str = "tt") -> str:
    """Return the .include line for a given corner."""
    return SPICE_MODELS.get(corner, SPICE_MODELS["tt"])


# ---------------------------------------------------------------------------
# Read SNM (Static Noise Margin) testbench
# ---------------------------------------------------------------------------

READ_SNM_TEMPLATE = Template("""\
* Read Static Noise Margin (SNM) Testbench
* Generated by rekolektion
*
* Measures the butterfly curve by sweeping a DC noise source
* inserted at each storage node.
*

.param vdd_val = ${vdd}
.param temp_val = ${temp}

* Include SKY130 models
.lib "${pdk_root}/sky130A/${model_path}" ${corner}

* Include bitcell subcircuit
.include "${bitcell_spice}"

* Supply
Vvdd VDD 0 DC vdd_val
Vvss VSS 0 DC 0

* Bit lines precharged to VDD (read condition)
Vbl  BL  0 DC vdd_val
Vblb BLB 0 DC vdd_val

* Word line ON
Vwl  WL  0 DC vdd_val

* Instantiate bitcell
Xcell BL BLB WL VDD VSS ${subckt_name}

* DC noise sources for butterfly curve
* Sweep Vn1 at Q node, measure QB; then sweep Vn2 at QB, measure Q
Vn1 Q_probe Xcell.Q DC 0
Vn2 QB_probe Xcell.QB DC 0

* Butterfly curve: sweep noise source, measure inverse transfer
.dc Vn1 0 vdd_val 0.01

* Measurements
.meas DC snm_high FIND V(Xcell.QB) WHEN V(Q_probe)=V(Xcell.QB) RISE=1
.meas DC snm_low  FIND V(Xcell.QB) WHEN V(Q_probe)=V(Xcell.QB) FALL=1

.control
run
set hcopypscolor = 1
set color0 = white
set color1 = black
set color2 = blue
set color3 = red
plot V(Xcell.QB) vs V(Q_probe) title "Read SNM Butterfly Curve"
wrdata ${output_prefix}_read_snm.csv V(Xcell.QB) vs V(Q_probe)
.endc

.end
""")


